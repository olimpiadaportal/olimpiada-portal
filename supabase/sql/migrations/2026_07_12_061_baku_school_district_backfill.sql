do $$
declare
  v_city uuid;
  v_assigned int; v_kept int; v_review int; v_sport int;
begin
  select id into v_city from public.districts where name like 'Bak%' limit 1;
  if v_city is null then
    raise notice 'backfill skipped: no Baku city row';
    return;
  end if;

  -- Special case first: №1 sport lyceum → Nizami.
  update public.schools s
     set city_district_id = cd.id
    from public.city_districts cd
   where s.district_id = v_city and s.school_number = 1
     and s.name ilike '%idman%'
     and s.city_district_id is null
     and cd.city_id = v_city and cd.name = 'Nizami';
  get diagnostics v_sport = row_count;

  -- Main pass: number-matched, never overwriting an existing assignment.
  update public.schools s
     set city_district_id = cd.id
    from _baku_map m
    join public.city_districts cd on cd.name = m.rayon
   where s.district_id = v_city
     and cd.city_id = v_city
     and s.school_number = m.school_number
     and s.city_district_id is null
     and not (s.school_number = 1 and s.name ilike '%idman%');
  get diagnostics v_assigned = row_count;

  select count(*) into v_kept from public.schools s
   where s.district_id = v_city and s.city_district_id is not null;
  select count(*) into v_review from public.schools s
   where s.district_id = v_city and s.city_district_id is null;

  raise notice 'Baku backfill report: % newly assigned (+% sport-lyceum special), % Baku schools now assigned total, % left for manual review (district IS NULL).',
    v_assigned, v_sport, v_kept, v_review;
end $$;

